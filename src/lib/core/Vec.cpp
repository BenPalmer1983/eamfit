#include "Vec.h"
/*****************************************************************************/


Vec::Vec(double x, double y, double z)
{
    Vec::set_xyz(x, y,  z);
}


void Vec::set_x(double x)
{    
    Vec::x = x;
}

void Vec::set_y(double y)
{    
    Vec::y = y;
}

void Vec::set_z(double z)
{    
    Vec::z = z;
}

void Vec::set_xyz(double x, double y, double z)
{    
    Vec::x = x;
    Vec::y = y;
    Vec::z = z;
}


